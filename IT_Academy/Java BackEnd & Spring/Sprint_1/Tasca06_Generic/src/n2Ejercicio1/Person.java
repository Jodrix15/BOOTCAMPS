package n2Ejercicio1;

public class Person {
	
	private String name;
	private String surname;
	private int yOld;
	
	public Person() {
		this.name = "Jordi";
		this.surname = "Hornos";
		this.yOld = 19;
	}

	public String getName() {
		return name;
	}

	public String getSurname() {
		return surname;
	}

	public int getyOld() {
		return yOld;
	}
	
	

}